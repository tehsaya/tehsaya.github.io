<h3>Sitting Still in a Chair</h3>
<img class="pull-right" src="images/concentration/sittingstill.png">
<p>Sit in a comfortable chair and see how still you can keep. This is not as easy as it seems. You will have to center your attention on sitting still. Watch and see that you are not making any involuntary muscular movements. By a little practice you will find you are able to sit still without a movement of the muscles for fifteen minutes. At first I advise sitting in a relaxed position for five minutes. After you are able to keep perfectly still, increase the time to ten minutes and then to fifteen. This is as long as it is necessary. But never strain yourself to keep still. You must be relaxed completely. You will find this habit of relaxing is very good.</p>
</div>


<div id="e2" class="clearfix">
<h3>Fix Gaze on an Object</h3>
  <img class="pull-left" src="images/concentration/gaze.png ">

<p>Sit in a chair with your head up and your chin out, shoulders back. Raise your right arm until it is on the level with your shoulder, pointing to your right. Look around, with head only, and fix your gaze on your fingers, and keep the arm perfectly still for one minute. Do the same exercise with your left arm. When you are able to keep the arm perfectly steady, increase the time until you are able to do this five minutes with each arm. Turn the palm of the hand downward when it is outstretched, as this is the easiest position. If you will keep your eyes fixed on the tips of the fingers you will be able to tell if you are keeping your arm perfectly still.</p>
</div>
