<h3>Concentration Increases the Sense of Smell</h3>
   <img class="pull-right" src="images/concentration/smell.png ">
<p>When you take a walk, or drive in the country, or pass a flower garden, concentrate on the odor of flowers and plants. See how many different kinds you can detect. Then choose one particular kind and try to sense only this. You will find that this strongly intensifies the sense of smell. This differentiation requires, however, a peculiarly attentive attitude. When sense of smell is being developed, you should not only shut out from the mind every thought but that of odor, but you should also shut out cognizance of every odor save that upon which your mind, for the time, is concentrated. You can find plenty of opportunity for exercises for developing the sense of smell. When you are out in the air, be on the alert for the different odors. You will find the air laden with all kinds, but let your concentration upon the one selected be such that a scent of its fragrance in after years will vividly recall the circumstances of this exercise.</p>
